UPDATE LOADER_NODES SET
NODE_X = (-(SUBSTR(NODE_X,1,3))-(SUBSTR(NODE_X,4,2) / 60)-
      (SUBSTR(NODE_X,6,5) / 3600)),
LONGITUDE = (-(SUBSTR(LONGITUDE,1,3))-(SUBSTR(LONGITUDE,4,2) / 60)-
      (SUBSTR(LONGITUDE,6,5) / 3600)),
NODE_Y = (SUBSTR(NODE_Y,1,2)+(SUBSTR(NODE_Y,3,2) / 60)+
      (SUBSTR(NODE_Y,5,5) / 3600)),
LATITUDE = (SUBSTR(LATITUDE,1,2)+(SUBSTR(LATITUDE,3,2) / 60)+
      (SUBSTR(LATITUDE,5,5) / 3600));
